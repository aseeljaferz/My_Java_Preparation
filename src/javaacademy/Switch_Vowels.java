package javaacademy;
import java.util.*;
public class Switch_Vowels {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		char vowel = in.next().charAt(0);
		
		switch(vowel) {
		
		case 'a':
			System.out.println("It is a vowel");
			break;
		case 'e':
			System.out.println("It is a vowel");
			break;
		case 'i':
			System.out.println("It is a vowel");
			break;
		case 'o':
			System.out.println("It is a vowel");
			break;
		case 'u':
			System.out.println("It is a vowel");
			break;
		case 'A':
			System.out.println("It is a vowel");
			break;
		case 'E':
			System.out.println("It is a vowel");
			break;
		case 'I':
			System.out.println("It is a vowel");
			break;
		case 'O':
			System.out.println("It is a vowel");
			break;
		case 'U':
			System.out.println("It is a vowel");
			break;
		default:
			System.out.println("Not a vowel");
			break;
		
		}

	}

}
